# WAP to show use of for loop

colour = ["Red","Green","Blue","Yellow"]
for color in colour:
    print(color)
    for i in color:
        print(i)

print("\n")

# WAP to show use of range function
for k in range(5):
    print(k)
print("\n")
for k in range(1,10):
    print(k)
print("\n")
for k in range(1,12,3):
    print(k)